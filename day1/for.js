// count to 10

// while loops
let counter = 0;

while (counter <= 10) {
  console.log(counter);
  counter++;
}

// for loops
// for (initialization; condition; iterator)
for (let counter=0; counter<=10; counter++) {
  console.log(counter);
}

// print the numbers between 0 and 100 using a for loop
for (let counter=0; counter<=100; counter++) {
  console.log(counter);
}

